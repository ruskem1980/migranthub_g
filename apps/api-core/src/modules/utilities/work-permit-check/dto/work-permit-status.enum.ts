/**
 * Статус разрешения на работу (РНР)
 */
export enum WorkPermitStatus {
  /** Разрешение действительно */
  VALID = 'VALID',
  /** Разрешение недействительно (аннулировано) */
  INVALID = 'INVALID',
  /** Срок действия разрешения истек */
  EXPIRED = 'EXPIRED',
  /** Разрешение не найдено в базе */
  NOT_FOUND = 'NOT_FOUND',
  /** Статус неизвестен (ошибка проверки) */
  UNKNOWN = 'UNKNOWN',
}

/**
 * Источник данных для результата проверки
 */
export enum WorkPermitSource {
  /** Результат получен от сервиса ФМС */
  FMS = 'fms',
  /** Результат получен из кэша */
  CACHE = 'cache',
  /** Fallback результат (mock или при недоступности сервиса) */
  FALLBACK = 'fallback',
}
