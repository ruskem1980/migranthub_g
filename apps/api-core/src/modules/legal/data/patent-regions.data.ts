/**
 * Get region by code
 */
export function getPatentRegionByCode(code: string): PatentRegion | undefined {
  return patentRegions.find((r) => r.code === code);
}

/**
 * Find region by city name (case-insensitive)
 */
export function findRegionByCity(cityName: string): PatentRegion | undefined {
  const searchName = cityName.toLowerCase().trim();
  return patentRegions.find((r) =>
    r.cities.some((city) => city.toLowerCase() === searchName)
  );
}

/**
 * Search regions and cities (returns matching regions)
 */
export function searchRegions(query: string): PatentRegion[] {
  if (!query.trim()) return patentRegions;

  const searchQuery = query.toLowerCase().trim();

  return patentRegions.filter(
    (r) =>
      r.name.toLowerCase().includes(searchQuery) ||
      r.code.includes(searchQuery) ||
      r.cities.some((city) => city.toLowerCase().includes(searchQuery))
  );
}

/**
 * Calculate patent cost for a region
 */
export function calculatePatentCost(regionCode: string, months: number): number | null {
  const region = getPatentRegionByCode(regionCode);
  if (!region) return null;
  return region.monthlyCost * months;
}

/**
 * Get all cities as flat list with region reference
 */
export function getAllCities(): Array<{ city: string; region: PatentRegion }> {
  const result: Array<{ city: string; region: PatentRegion }> = [];
  for (const region of patentRegions) {
    for (const city of region.cities) {
      result.push({ city, region });
    }
  }
  return result.sort((a, b) => a.city.localeCompare(b.city, 'ru'));
}
